//Operadores Aritméticos + - / * 
/* 
Aritméticos
 + Adição / Concatenação
 - subtração
 / divisão
 * multiplicação
 ** potenciação
 % resto da divisão
*/

const num1 = 5;
const num2 = 10;
console.log(num1 + num2); //adição ou soma + (concatenação)

const num3 = '4'; // entre aspas é uma string e não number
const num4 = 14;
console.log(num3 + num4);

const num5 = 7;
const num6 = 14;
console.log(num5 - num6); //subtração -

const num7 = 20;
const num8 = 40;
console.log(num7 / num8); //divisão / 

const num9 = 2;
const num0 = 10;
console.log(num9 * num0); //multiplicação *

const num10 = 13;
const num11 = 7;
console.log(num10 ** num11); //potenciação **

const num12 = 12;
const num13 = 7;
console.log(num12 % num13); //resto da divisão % 